#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*result;
	int		i;
	int		j;
	int		len;

	i = 0;
	len = ft_strlen(s1) + ft_strlen(s2);
	result = (char *)malloc((sizeof(char)) * (len + 1));
	if (!result)
		return (NULL);
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		result[i] = s2[j];
		j++;
		i++;
	}
	result[len] = '\0';
	return (result);
}
